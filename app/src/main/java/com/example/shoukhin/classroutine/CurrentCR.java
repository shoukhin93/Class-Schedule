public class CurrentCR {

    public String name;

    public String roll;

    public CurrentCR() {
    }

    public CurrentCR(String name, String roll) {
        this.name = name;
        this.roll = roll;
    }

    public String getName() {
        return name;
    }



    public String getRoll() {
        return roll;
    }

}
